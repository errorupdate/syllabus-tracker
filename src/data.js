// Flatten all PDFs with unique IDs for tracking
let pdfCounter = 0;
export function getAllPdfs() {
  const result = [];
  for (const subject of SUBJECTS) {
    for (const topic of subject.topics) {
      if (topic.chapters) {
        for (const chapter of topic.chapters) {
          for (const pdf of chapter.pdfs) {
            result.push({ id: `pdf-${pdfCounter++}`, name: pdf, chapterId: chapter.id, topicId: topic.id, subjectId: subject.id });
          }
        }
      } else if (topic.pdfs) {
        for (const pdf of topic.pdfs) {
          result.push({ id: `pdf-${pdfCounter++}`, name: pdf, topicId: topic.id, subjectId: subject.id });
        }
      }
    }
  }
  pdfCounter = 0;
  return result;
}

export function getTotalPdfCount() {
  return getAllPdfs().length;
}
